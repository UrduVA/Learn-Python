# Enter day number and get day name
day = int(input("Enter a day number (1-7): "))
if day == 1:
    print("Sun")
elif day == 2:
    print("Mon")
elif day == 3:
    print("Tue")
elif day == 4:
    print("Wen")
elif day == 5:
    print("Thr")
elif day == 6:
    print("Fri")
elif day == 7:
    print("Sat")
else:
    print("Invalid Choice: ")
        

